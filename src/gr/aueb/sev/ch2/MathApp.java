package gr.aueb.sev.ch2;

/**
 * Demo της κλάσης Math.
 */
public class MathApp {

    public static void main(String[] args) {
        System.out.println("Το απόλυτο του -5 είναι " + Math.abs(-5));
        System.out.println("Το μικρότερο μεταξύ 1 και 10 είναι: " + Math.min(1, 10));
        System.out.println("Το μεγαλύτερο μεταξύ 1 και 10 είναι: " + Math.max(1, 10));
    }
}
